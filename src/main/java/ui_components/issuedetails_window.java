    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        int issue_group = combobox_state.getSelectedIndex();
        try{
            Issue_Connector ic = new Issue_Connector(connector);
            Parser parser = new Parser(ic.update_group(issue_id, issue_group, this));
            if (parser.get_int("flag") == 1){
                new message_window(this,true,"Group state value updated","");
                load_window();
            }
        } catch (UnirestException ex) {
            new message_window(this,true,"Error\n"+ex.toString(),"ERROR");
        }
    }//GEN-LAST:event_button_updateActionPerformed

