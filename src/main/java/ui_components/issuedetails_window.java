    /**
     * Function for loading window components
     */
    void load_window() throws UnirestException{
        Issue_Connector ic = new Issue_Connector(connector);
        Parser parser = new Parser(ic.issue_get(issue_id, this));
        Parser user = new Parser(connector.get_user(parser.get_int("user_id"), this));
        this.setTitle("Issue - "+parser.get_string("issue_name")+" - details");
        label_issuename.setText(parser.get_string("issue_name"));
        load_combobox(parser.get_int("issue_group"));
        label_status.setText(parser.get_string("issue_state"));
        label_owner.setText(user.get_string("user_login"));
        label_priority.setText(Integer.toString(parser.get_int("issue_priority")));
        label_projectid.setText(Integer.toString(parser.get_int("project_id")));
        field_desc.setText(parser.get_string("issue_desc"));
        field_desc.setEditable(false);
        
        switch(label_status.getText()){
            case "UNDONE":
                label_status.setForeground(Color.red);
                break;
            case "DONE":
                label_status.setForeground(Color.green);
                combobox_state.setEnabled(false);
                button_setdone.setEnabled(false);
                break;
        }
    }
    
    /**
     * Function for loading combobox
     */
    void load_combobox(int mode){
        DefaultComboBoxModel dcm = new DefaultComboBoxModel();
        dcm.addElement("Untouched");
        dcm.addElement("Planned");
        dcm.addElement("In work");
        dcm.addElement("Done");
        combobox_state.setModel(dcm);
        combobox_state.setSelectedIndex(mode);
    }

