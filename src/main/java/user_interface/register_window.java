    /**
     * Function for vield validation
     */
    boolean validate_fields(){
        if ( !field_name.getText().equals("") ){
           if ( !field_surname.getText().equals("")){
               if ( !field_email.getText().equals("") && field_email.getText().contains("@")){
                   return true;
               }
               return false;
           }
           return false;
        }
        return false;
        
    }

