    private void button_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_registerActionPerformed
        try {
            if ( validate_fields() ){
                JsonElement response = connector.user_register(field_name.getText(), field_surname.getText(), field_email.getText());
                if ( response != null ){
                    Parser parser = new Parser(response);
                    if ( !parser.get_string("user_password").equals("") ){
                        new message_window(this,true,"User registered!","");
                        dispose();
                    }
                }
                else{
                    new message_window(this,true,"Error registering user. Check log","");
                }
            }
            else{
                new message_window(this,true,"Error validating fields. Check fields","");
            }
        } catch (UnirestException ex) {
            Logger.getLogger(register_window.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button_registerActionPerformed


